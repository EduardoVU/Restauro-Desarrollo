const numero2 = "20";


console.log(parseInt(numero2));
console.log(numero2);
console.log(numero1.toString());

//parseInt() es una función porque tiene parámetros dentro de paréntesis
//toString es un metodo, porque va el nombre de la variable antes de un punto y no tiene parámetros 

